t=-10:10;
delta=[zeros(1,10),ones(1,11)];
subplot(3,1,1);
stem(t,delta);
axis([-10 10 -1.5 1.5]);
title('\delta[n]');
xlabel('n');
ylabel('Amplitude');
subplot(3,1,2);
deltam2=[zeros(1,2),delta(1:length(delta)-2)];
stem(t,deltam2);
axis([-10 10 -1.5 1.5]);
title('\delta[n-2]');
xlabel('n');
ylabel('Amplitude');
subplot(3,1,3);
deltap2=[delta(3:length(delta)),zeros(1,2)];
